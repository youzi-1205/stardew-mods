using StardewValley;

namespace AutoSort;

/// <summary>把一件物品归入 8 个大类之一。分类号对照 <see cref="StardewValley.Object"/> 的全部
/// 分类常量逐一归并（数值多年稳定，这里直接用字面量避免版本耦合）：相关的多种游戏分类合进同一个
/// 大类，鞋子/衣服/戒指/武器/工具等归到「装备用品」、家具书籍单成一类，只有真正的垃圾才进「杂物」，
/// 避免分类爆炸、也不无脑乱丢。</summary>
internal static class Categorizer
{
    public static string Resolve(Item item)
    {
        return item.Category switch
        {
            // 矿石宝石：宝石(-2)、矿物(-12)、矿石/金属锭/煤(-15)
            -2 or -12 or -15 => "矿石宝石",

            // 采集种植：野菜菇(-81)、花(-80)、蔬菜(-75)、水果(-79)、种子(-74)、肥料(-19)
            -81 or -80 or -75 or -79 or -74 or -19 => "采集种植",

            // 食物：鱼(-4)、蛋(-5)、奶(-6)、肉(-14)、动物制品(-18)、熟食(-7)、烹饪食材(-25)
            -4 or -5 or -6 or -14 or -18 or -7 or -25 => "食物",

            // 工匠品：酒/奶酪等(-26)、糖浆(-27)
            -26 or -27 => "工匠品",

            // 资源材料：木石纤维等建材(-16)、工艺材料(-8)、大型机器(-9)、怪物战利品(-28)
            -16 or -8 or -9 or -28 => "资源材料",

            // 装备用品：武器(-98)、工具(-99)、戒指(-96)、靴子(-97)、帽子(-95)、衣服(-100/-94)、
            //          饰品(-101)、装备(-29)、鱼饵(-21)、渔具(-22)
            -98 or -99 or -96 or -97 or -95 or -100 or -94 or -101 or -29 or -21 or -22 => "装备用品",

            // 家具书籍：家具(-24)、书(-102)、技能书(-103)
            -24 or -102 or -103 => "家具书籍",

            // 杂物：只剩真正的杂项——垃圾(-20)、地面杂物(-999)、不可食用(-300)、特殊售卖类(-17/-23)及未分类
            _ => "杂物",
        };
    }
}
